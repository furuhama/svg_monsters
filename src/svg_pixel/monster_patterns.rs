#![allow(unused)]
extern crate rand;

use self::patterns::{ARMS, BODIES, EYES, HAIRS, LEGS, MOUTHS};
use self::rand::{thread_rng, Rng};

#[derive(Debug)]
pub struct MonsterPattern {
    leg: Vec<Vec<bool>>,
    hair: Vec<Vec<bool>>,
    arm: Vec<Vec<bool>>,
    body: Vec<Vec<bool>>,
    eye: Vec<Vec<bool>>,
    mouth: Vec<Vec<bool>>,
}

impl MonsterPattern {
    fn new() -> Self {
        Self {
            leg: Vec::<Vec<bool>>::new(),
            hair: Vec::<Vec<bool>>::new(),
            arm: Vec::<Vec<bool>>::new(),
            body: Vec::<Vec<bool>>::new(),
            eye: Vec::<Vec<bool>>::new(),
            mouth: Vec::<Vec<bool>>::new(),
        }
    }

    fn init_randomly() -> Self {
        let mut monster_pattern = Self::new();

        let mut rng = thread_rng();

        monster_pattern.set_leg(array_to_vec(&LEGS[rng.gen_range(0, LEGS.len())]));
        monster_pattern.set_hair(array_to_vec(&HAIRS[rng.gen_range(0, HAIRS.len())]));
        monster_pattern.set_arm(array_to_vec(&ARMS[rng.gen_range(0, ARMS.len())]));
        monster_pattern.set_body(array_to_vec(&BODIES[rng.gen_range(0, BODIES.len())]));
        monster_pattern.set_eye(array_to_vec(&EYES[rng.gen_range(0, EYES.len())]));
        monster_pattern.set_mouth(array_to_vec(&MOUTHS[rng.gen_range(0, MOUTHS.len())]));

        monster_pattern
    }

    pub fn generate_ramdomly() -> Vec<Vec<bool>> {
        let monster_pattern = Self::init_randomly();

        monster_pattern.unite()
    }

    fn unite(&self) -> Vec<Vec<bool>> {
        let mut shape = self.unite_shape();
        let face_parts = self.unite_face_parts();

        for y in 0..shape.len() {
            for x in 0..shape[y].len() {
                if face_parts[y][x] {
                    shape[y][x] = false;
                }
            }
        }

        shape
    }

    fn unite_shape(&self) -> Vec<Vec<bool>> {
        let mut shape = union(&self.leg, &self.hair);
        shape = union(&shape, &self.arm);
        shape = union(&shape, &self.body);

        shape
    }

    fn unite_face_parts(&self) -> Vec<Vec<bool>> {
        union(&self.eye, &self.mouth)
    }

    fn set_leg(&mut self, leg: Vec<Vec<bool>>) {
        self.leg = leg;
    }

    fn set_hair(&mut self, hair: Vec<Vec<bool>>) {
        self.hair = hair;
    }

    fn set_arm(&mut self, arm: Vec<Vec<bool>>) {
        self.arm = arm;
    }

    fn set_body(&mut self, body: Vec<Vec<bool>>) {
        self.body = body;
    }

    fn set_eye(&mut self, eye: Vec<Vec<bool>>) {
        self.eye = eye;
    }

    fn set_mouth(&mut self, mouth: Vec<Vec<bool>>) {
        self.mouth = mouth;
    }
}

fn union(one: &[Vec<bool>], other: &[Vec<bool>]) -> Vec<Vec<bool>> {
    let mut result = Vec::<Vec<bool>>::with_capacity(one.len());

    for y in 0..one.len() {
        let mut result_y = Vec::<bool>::with_capacity(one[y].len());

        for x in 0..one[y].len() {
            if !one[y][x] && !other[y][x] {
                result_y.push(false);
            } else {
                result_y.push(true);
            }
        }

        result.push(result_y);
    }

    result
}

fn array_to_vec(arr: &[[u8; 12]; 12]) -> Vec<Vec<bool>> {
    let mut vec = Vec::<Vec<bool>>::with_capacity(12);

    for ar in arr.iter() {
        let mut v = Vec::<bool>::with_capacity(12);

        for e in ar.iter() {
            if *e == 0 {
                v.push(false);
            } else {
                v.push(true);
            }
        }

        vec.push(v);
    }

    vec
}

